def main():
    for lang in ['en', 'hi', 'kn', 'ta', 'ml', 'or']:
        vocab_path = os.path.join(PROJECT_DIR, "translations", f"translations_{lang}.json")
        if not os.path.exists(vocab_path):
            continue
        print(f"Updating translations_{lang}.json with panchangam terms...")
        with open(vocab_path, 'r', encoding='utf-8') as f:
            data = json.load(f)

        # 1. Add 60 Samvatsara names
        for i, te_name in enumerate(SAMVATSARAS):
            data[te_name] = SAMVATSARA_TRANS[lang][i]

        # 2. Add 12 Telugu masam names
        for i, te_name in enumerate(MASALU):
            data[te_name] = MASAM_TRANS[lang][i]

        # 3. Add misc terms
        for k, v in MISC_TERMS[lang].items():
            data[k] = v

        with open(vocab_path, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)

    print("All panchangam translations applied successfully!")

if __name__ == "__main__":
    main()
